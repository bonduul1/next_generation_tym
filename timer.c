uint8_t start_timers()
{
  uint8_t ret = 1;
  
  HAL_TIM_OC_Start(&htim3, TIM_CHANNEL_4);
  HAL_TIM_OC_Start(&htim4, TIM_CHANNEL_3);
  HAL_TIM_OC_Start(&htim4, TIM_CHANNEL_4);

  return ret;
}

uint8_t update_tim_channel(uint8_t channel, uint16_t duty)
{
  if(channel == TIMER_CHANNEL_3) {
    htim4.Instance->CCR3 = duty;
  }
  else if(channel == TIMER_CHANNEL_4) {
    htim4.Instance->CCR4 = duty;
  }
  return 1;
}

uint8_t update_tim_pto(uint16_t duty)
{
  htim3.Instance->CCR4 = duty;
  return 1;
}